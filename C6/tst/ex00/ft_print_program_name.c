#include <unistd.h>
#include <stdio.h>

int	main(int ac, char **av)
{
	int	i;

	i = 0;
	while (ac == 1 && av[0][i] != '\0')
	{
		write(1, &av[0][i], 1);
		i++;
	}
	write (1, "\n", 1);
}
